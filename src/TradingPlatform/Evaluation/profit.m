
function performance = profit(positionsLog)

% [ ...
%     positionType, ...
%     openIndex, ...
%     closeIndex, ...
%     openDate, ...
%     closeDate, ...
%     openPrice, ...
%     closePrice, ...
%     profitLoss ...
% ] ...
% = positionsLog;

profit = positionsLog(positionsLog(:,8)>1,8);

if isempty(profit)
    performance = 0;
else
    performance = prod(profit);
end

end
