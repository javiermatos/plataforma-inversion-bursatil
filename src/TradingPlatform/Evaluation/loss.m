
function performance = loss(positionsLog)

% [ ...
%     positionType, ...
%     openIndex, ...
%     closeIndex, ...
%     openDate, ...
%     closeDate, ...
%     openPrice, ...
%     closePrice, ...
%     profitLoss ...
% ] ...
% = positionsLog;

loss = positionsLog(positionsLog(:,8)<1,8);

if isempty(loss)
    performance = 0;
else
    performance = prod(loss);
end

end
